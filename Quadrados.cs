namespace AgenteInteligente
{
    public class Quadrados 
    {
        public Quadrado quadradoA;
        public Quadrado quadradoB;
        public Quadrado quadradoC;
        public Quadrado quadradoD;
        public Quadrados(Quadrado quadradoA, Quadrado quadradoB, Quadrado quadradoC, Quadrado quadradoD)
        {
            this.quadradoA = quadradoA;
            this.quadradoB = quadradoB;
            this.quadradoC = quadradoC;
            this.quadradoD = quadradoD;
        }
    }
}